#include <stdio.h> // standard input output library (標準輸入輸出函式庫)

int main()
{
    int a = 10;
    int b = 8;
    
    printf("a = %d,b = %d\n",b,a); // 一個蘿蔔一個坑

    int age = 21;
    float height = 176.5;
    float weight = 75;

    printf("[age] %d [Height] %.2f cm [Weight] %.1f kg \n",age,height,weight);

    return 0;
}

